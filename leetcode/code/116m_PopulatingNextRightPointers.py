# Definition for a Node.
class Node:
    def __init__(self, val: int = 0, left: 'Node' = None, right: 'Node' = None, next: 'Node' = None):
        self.val = val
        self.left = left
        self.right = right
        self.next = next


class Solution:
    def connect(self, root: 'Node') -> 'Node':

        if not root:
            return None
        self.connect_two_node(root.left, root.right)
        return root

    def connect_two_node(self, node_1, node_2):
        """定义递归子函数含义：连接传进去的每两个相邻节点"""
        if not node_1 and not node_2:
            return None
        node_1.next = node_2
        self.connect_two_node(node_1.left, node_1.right)
        self.connect_two_node(node_2.left, node_2.right)
        self.connect_two_node(node_1.right, node_2.left)
